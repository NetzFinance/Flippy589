	const [mute, setMute] = useState(muteDefault)
	
	const text = textOptions[textIndex]
	const color = colorOptions[colorIndex]
		
	const handleClick = () => {
		// play the animation 		
		flippyTheSwitch()
		
		// play with switch sound clip
		if (!mute) playSwitchSound()
		
		// play the song at the end
		if ((textIndex == (textOptions.length - 2)) && !mute) {
			playMusic()
		}
		
		// make Flippy dance at the end
		if ((textIndex == (textOptions.length - 2))) {
			setTimeout(() => { playFlippyGif() }, 1150)
		}
	
		// increment the text / phrases
		let newTextIndex = textIndex + 1;
    if (newTextIndex < textOptions.length) {
			setTextIndex(newTextIndex)
		} else {
			setTextIndex(0)
			clearColorRotation()
			
		}
		
		// increment the color if the colorCounter (color shuffle) isn't running 
		if (colorCounter == 0) {
			let newColorIndex = colorIndex + 1;
			if (newColorIndex < colorOptions.length) {
				setColorIndex(newColorIndex)
			} else {
				setColorIndex(0)
				clearColorRotation()
			}
			
			// remove red color index from the shuffle 
			// on the first play so we don't repeat it
			if (textIndex == (textOptions.length - 2)) {
				shuffleIndex.splice(1, 1)
			}
		}
	}
	
